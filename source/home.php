                <h2>Summary</h3><br><br>
                <p>‘The Tempest‘ begins with a tempest, and there are no surprises because the name suggests the same.
                    The passengers of a ship witness a massive storm at sea which leaves them shipwrecked and scattered on a mysterious island.<br><br>

                    This is a classic Shakespearean opening where things are well and truly shaken up.
                    We then move to the shore where we meet Prospero, a powerful wizard who rules the island.
                    It is a magical island and he and his daughter Miranda are the only humans who live there.<br><br>

                    As they look out at the shipwreck Prospero tells Miranda that he was once Duke of Milan but he was betrayed by his evil brother,
                    Antonio and the Duke of Naples, Alonso. Prospero and Miranda were exiled and would have died if Gonzalo, a nobleman hadn’t helped them in secret.<br><br>

                    Prospero tells Miranda that Antonio and Alonso were on the shipwrecked boat and that he created the storm to get revenge on them.<br><br>

                    We also find out that Prospero has two servants the air spirit, Ariel who has been helping him carry out his revenge and Caliban,
                    the ugly son of an evil witch who owned the island and died before Prospero arrived.<br><br>

                    In exchange for carrying out Prospero’s master plan, Prospero promises to free Ariel after two days if he remains obedient.
                    The first member of the shipwreck we find is prince Ferdinand, the son of Alonso. Ariel who has made himself invisible guides Ferdinand toward Miranda. 
                    When they meet, it is love at first sight. Prospero is delighted as he intends for the two to get married however, he doesn’t want the relationship to 
                    progress too quickly so he sends Ferdinand off to the hall wood forcing him to prove his love through hard labour.<br><br>

                    On another part of the island are the remaining noblemen of Milan. There’s Antonio, Gonzalo, Alonso and Alonso’s brother, Sebastian. 
                    Ariel uses a magical song to put everyone to sleep except for Sebastian and Antonio, the two then begin to discuss a scheme to murder Gonzalo and Alonso to help their political careers.<br><br>

                    They draw their swords but Ariel saves the day waking everyone up in the nick of time Sebastian makes up a ridiculous story to explain the swords which nobody buys. 
                    They decide to travel around the island to look for Ferdinand as if that wasn’t enough intrigue.<br><br>

                    We find out what’s been happening with the third and final group Caliban has fallen in with Trinculo, a jester and Stefano, a butler. 
                    The three of them get raucously drunk and decide to kill Prospero but they don’t get far.<br><br>

                    Later we find out that Ariel distracts them and leads them into a stinky swamp, meanwhile after a long day of travelling the group of nobles stops and takes a rest. 
                    A magical feast appears which an invisible Prospero has conjured.<br><br>

                    As they sit down to eat, Ariel appears as a horrible harpy part woman, part bird and makes the food disappear. 
                    He also tells Alonso that the sea has taken his son as punishment for his crimes. Alonso is distraught who wouldn’t be back at Prospero’s house.<br><br>

                    He’s putting on a magic show for the newly engaged Ferdinand and Miranda but he suddenly remembers Caliban’s plan to kill him and cuts the show short. 
                    Prospero and Ariel lure Caliban and his crew into Prospero’s cell with magical clothes then they conjure spirit hounds to chase them away and teach them a lesson.<br><br>

                    Prospero tries to stay on track with his grand plan. He asks Ariel how Alonso’s group are doing. 
                    Ariel says they’ve all gone mad and explains how Alonso is grieving the death of his son.<br><br>

                    Prospero realises that his revenge is causing real harm and tells Ariel to gather them on the beach. 
                    Prospero confronts the group with their individual crimes but says he will forgive them. 
                    He reveals that Ferdinand is still alive and Alonso is over the moon.<br><br>

                    Ferdinand tells his father about his marriage to Miranda. Just in time, Caliban’s crew arrive on the beach dripping wet and carrying the fine clothes they stole. 
                    Prospero orders Caliban to return to his cell to prepare a feast for the group as everything wraps up.<br><br>

                    It seems that order has been restored to the play’s universe. Prospero plans to return to Italy and reclaim his title as Duke of Milan. 
                    He gives Ariel one final task to make the seas calm for their return before setting him free.<br><br>

                    At the very end of the play, Prospero breaks the fourth wall and talks to the audience. 
                    He talks to you, yes you and asks you to set him free from the stage with your applause. 
                    He’s used enough magic to last a lifetime and so the spell is broken or is it?
